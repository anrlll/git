class Plus{
    public static void main(String[] args){
        int plus=0;
        for(int i=0;i<6;i++){
            plus += i;
        }
        System.out.println(plus);
        
        plus = 0;
        for(int i=0;i<5;i++){
            plus += i;
        }
        System.out.println(plus);

        
    }
}